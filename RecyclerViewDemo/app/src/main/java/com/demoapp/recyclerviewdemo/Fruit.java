package com.demoapp.recyclerviewdemo;

/**
 * 表示一个水果对象，包含资源图片和水果名
 */
public class Fruit {
    private String name;
    private int imageId;

    public Fruit(String name, int imageId) {
        this.name = name;
        this.imageId = imageId;
    }

    public String getName() {
        return name;
    }

    public int getImageId() {
        return imageId;
    }
}
